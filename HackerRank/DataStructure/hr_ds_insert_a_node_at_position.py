class SinglyLinkedListNode:
    def __init__(self, node_data):
        self.data = node_data
        self.next = None

class SinglyLinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def insert_node(self, node_data):
        node = SinglyLinkedListNode(node_data)

        if not self.head:
            self.head = node
        else:
            self.tail.next = node


        self.tail = node

def print_singly_linked_list(node, sep, fptr):
    while node:
        fptr.write(str(node.data))

        node = node.next

        if node:
            fptr.write(sep)


def insertNodeAtPosition(head, data, position):
    node = SinglyLinkedListNode(data)
    tmp = head
    i = 0
    if position > 0:
        while i+1 != position:
            tmp = tmp.next
            i += 1
        tmp1 = tmp.next    
        tmp.next = node
        node.next = tmp1
        return head
    else:
        node.next = head
        return node
    
